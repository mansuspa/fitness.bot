def get_ai_response(question: str, goal: str = "maintain", profile: dict = None) -> str:
    """
    Ищет ответ в базе знаний по ключевым словам.
    Возвращает None если не нашёл ничего подходящего.
    """
    q = question.lower()

    # Поиск по ключевым словам
    for topic, data in KB.items():
        for keyword in data["keywords"]:
            if keyword in q:
                answer = random.choice(data["answers"])
                return answer

    # Ничего не нашли
    return None


